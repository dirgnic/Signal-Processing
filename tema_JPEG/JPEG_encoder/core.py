def enc8(b, Q): return np.round(dctn(b.astype(np.float64), norm='ortho')/Q)
def dec8(q, Q): return idctn(q*Q, norm='ortho')
def scaleQ(Q, qf):
    qf = max(1, min(100, qf))
    s = 5000//qf if qf < 50 else 200-(qf<<1)
    return np.clip(np.floor((Q*s+50)/100), 1, 255)
